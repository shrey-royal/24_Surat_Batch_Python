def addition(a, b):
    return a+b

def subtraction(a, b):
    return a-b
    
def multiplication(a, b):
    return a*b

def division(a, b):
    return a/b

def main():
    print(addition(10, 2))
    print(subtraction(10, 2))
    print(multiplication(10, 2))
    print(division(10, 2))


if __name__ == "__main__":  # main block
    main()